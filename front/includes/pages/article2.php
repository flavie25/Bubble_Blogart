            Savez vous ce que sont des mascarons ? Avez vous déjà pris le temps d’observer plus la ville de Bordeaux, allez plus loin que les apparences, et voir ces figures qui vous fixent dans le coin des bâtiments ? Dans tous les cas, je vous propose d’apprendre à découvrir Bordeaux sous un nouveau jour !
            </p>
            <p style="font-weight: bold;">
                Entre burlesque et baroque, voici venir le vrai visage de la Belle Endormie !
            </p>
            <h2>
                Les Mascarons de Bordeaux, figures du XVIe siècle :
            </h2>
            <p>
            Avez-vous déjà remarqué ces regards silencieux et intimidants qui nous fixent dans les rues bordelaises ? Ce sont des mascarons, vestiges d’une ancienne époque, masques burlesques piégés à tout jamais dans la pierre font partie intégrante de l’histoire de Bordeaux, mais également de l’histoire de la France. Avant d'arriver à Bordeaux, ces sculptures et ornements sont ciselés pour François 1er par de nombreux artistes, et, c’est ainsi que les mascarons se répandent dans toute la France. Ces figures, nommées “mascarons”, de l’italien “grand masque grotesque” et de l’arabe, “bouffonnerie”, sont des sculptures et ornements, formés d’une tête ou d’un masque. Leur fonction était de protéger les maisons. Ces masques étaient mis sur les façades des habitations pour repousser les mauvais esprits et ainsi, s’attirer les faveurs des dieux ! 
Ils arrivent donc dans notre ville à la fin du XVIe siècle par le maître maçon Henri Roche. Puis les mascarons se diffusent dans toute la ville au XVIIIe siècle grâce aux grands travaux entrepris par l’intendant Tourny (Si vous ne le connaissez pas, cet homme a permis à Bordeaux de se transformer, la ville lui doit beaucoup). Les mascarons ornent chaque recoin de la ville de Bordeaux, à tel point qu’on la surnomme “la ville aux masques”.

            </p>
            <h2>
            Un visage parmi tant d’autres : 
            </h2>
            <p>
            Reconnaître les différents mascarons de Bordeaux n’est pas simple, mais la bonne nouvelle, c’est que c’est possible de les différencier !
 Et oui, la plupart des mascarons de Bordeaux ont pour inspiration les dieux antiques, que l’on peut reconnaître grâce aux attributs qui ornent leurs cartouches. Ainsi, il est aisé de reconnaître une représentation divine. Vous allez impressionner votre famille et vos amis en leur montrant un mascaron et en leur donnant son identité ! Par exemple, Minerve est représentée avec une chouette à ses côtés, la foudre pour Jupiter, un trident et/ou un gouvernail pour Neptune. Parfois, vous pourrez voir des têtes animales, cornues pour les satyres, des lions édentés… Et aussi des représentations de femmes noires ! A votre avis, pourquoi des représentations de femmes noires ? Il faut remonter assez loin dans le temps, à l’époque du commerce triangulaire… Ça y est, vous voyez où je veux en venir ? Eh oui, ces représentations sont des références à la traite négrière. Les mascarons reflètent l’histoire de la ville : la colonisation et l’esclavage ont beaucoup participé à la richesse de Bordeaux, tant par la traite négrière que par le commerce de denrées cultiver par les esclaves. 
Ainsi, de nombreux mascarons Bordelais gardent la mémoire de cette histoire et représentent donc des visages africains.

            </p>
            <h2>
            Quelques balades :
            </h2>
            <p>
                Après toutes ces informations sur les mascarons, encore faut-il les trouver, savoir où ils sont…Pas facile de les apercevoir...
                De balade en balade, j’ai repéré un certain nombre de rues, où les mascarons sont nombreux, à peine visibles, cachés sur les façades des maisons, discrets, intimidants, comiques pour certains… Voici alors quelques parcours pour découvrir Bordeaux d’un nouvel œil ! Des balades “Fast” d'à peine 10 minutes, aux balades de plus de 30 minutes ! 
                <br>
                Balade Fast (~10 minutes):<br>
                Allées de Tourny -> Rue Mautrec -> Rue du pont de la Mousque -> Place Gabriel.<br>
                Trajet de 9 minutes à pied, 3 minutes en vélo, 700 mètres environ !<br>
                Rue du Mirail -> Cours Victor Hugo -> Place Pey Berland .<br>
                Trajet de 13 minutes à pied, 5 minutes en vélo, 1,1 km environ !<br>
                <br>
                Balade Simple (~20 minutes):<br>
                Rue de Pessac -> Rue Théodore Gardère -> Rue Leyteire.<br>
                Trajet de 23 minutes à pied, 10 minutes en vélo, 2,5 km environ !<br>
                Rue David Johnston -> Rue Camille Godard -> Cours de la Martinique.<br>
                Trajet de 21 minutes à pied, 8 minutes en vélo, 1,7 km environ !<br>
                <br>
                Balade Longue (~30 minutes ou plus):  <br>
                Cours de l’Yser -> Cours Aristide Briand -> Rue Malbec -> Rue Cadaujac -> Cours de la Somme.<br>
                Trajet de 33 minutes à pied, 12 minutes en vélo, 2,6 km environ !<br>
                Cours de la Somme -> Rue de Serpolet -> Rue des Trois Conils.<br>
                Trajet de 30 minutes à pied, 11 minutes en vélo, 2,3 km environ !<br>
                Rue Lachassaigne -> Rue de Mexico -> Rue de Capdeville.<br>
                Trajet de 39 minutes à pied, 17 minutes en vélo, 3,7 km environ !<br>
            </p>
            <h2>
                Conclusion : 
            </h2>
            <p>
            J’espère que je vous aurais été utile dans vos recherches de l’histoire de Bordeaux et vous aurait donné envie de visiter la ville sous un nouveau jour, ou plutôt, sous un nouveau regard !
            N'oubliez pas de partager les mascarons qui vous auront le plus plu, et où vous les avez trouver ! Et le plus important, n’hésitez pas à vous écarter des sentiers battus pour découvrir d’autres visages peu connus ! Il y en a environ 3000, rien qu’à Bordeaux !
            Arriverez-vous à tous les trouver ?

            </p>

            <p>
            Les Bassins de Lumières, niché dans la base sous-marine de Bordeaux, à ouvert ses portes au public en 2020, en tant que plus grand centre d’art numérique au monde, avec ces 14 000 m2 d’exposition, cet espace culturel est une mine d’or pour les férus d’art et de modernité.  Pourtant la base sous-marine renferme un passé sombre où se mélangent horreur et secret. L’art atténuera -t-il la noirceur des souvenirs anciens ? 
            </p>
            <p style="font-weight: bold;">
            Entre émerveillement et ignominie, quand art et histoire se mêlent, découvrez la face cachée des Bassins de Lumières.
            </p>
            <h2>
            Les Bassins de Lumières, le renouveau de la base sous marine
            </h2>
            <p>
            Aujourd’hui l’une des dernières traces de la Seconde Guerre mondiale à Bordeaux, ce bloc de béton représente un édifice convoité par les artistes. Après avoir abrité de petites entreprises en 1960, puis des expositions temporaires en 1990, on y trouve aujourd’hui de somptueuses expositions immersives. Les gigantesques alvéoles,de béton, d’acier et d'eau, où se déroulent les expositions, offrent au spectateurs un spectacle en 360.° Les tableaux se reflétant dans l’eau accentuent le ressenti féerique, mystique presque irréel de la scène. Sur la totalité de l'édifice 14 000 m2 sont ouverts au public. Les salles, toutes uniques, offrent chacune un point de vue différent sur l’art qu’elles projettent. L’espace et le lieu laissent place à une multitude de possibilités de projection. Les spectateurs ont pu admirer les œuvres de Gustav Klimt à partir du 10 juin 2020, accompagné musicalement par Luca Longobardi. Malheureusement, l’exposition à été écourtée à cause de l'épidémie actuelle. C’est avec impatience que nous attendons la réouverture pour pouvoir admirer les projections bleutées de Yves Klein et une exposition sur le thème de la Méditerranée, avec différents artistes comme Monet, Picasso, Renoir… 
Avec tout ça nous sommes d’accord que Culturespaces, qui a orchestré la remise à neuf de la base sous-marine de Bordeaux, nous offre un un espace culturel incroyable et moderne. Seulement, est-ce suffisant pour oublier le passé sinistre de ce lieu ? Les œuvres projetées sur les murs parviennent-elles à effacer les actes inhumains qui se sont déroulés à l'intérieur de ceux-ci ? 

            </p>
            <h2>
            Un passé coulé dans le béton
            </h2>
            <p>
            La construction de la base sous-marine de Bordeaux commence en 1941. Cet édifice de 45 000 m2 et de 600 000 m3 de béton armé fut un point stratégique pour les allemands durant la Seconde Guerre mondiale. Construite sous les ordres allemands, cette base est l’une des 5 construites sur le littoral français. 
La main-d'œuvre était constituée essentiellement de républicains espagnols ayant été forcés d’exil par le régime Franco, réfugiés en France pour trouver un peu de paix et de justice. Mais le pays est à l’aube de la Seconde Guerre mondiale et n’offre qu’une atmosphère pesante et hostile. En dépit des attentes des exilés, 275 000 républicains espagnols sont internés dans des camps de concentration en 1939,c’est la Retirada.
En 1941, alors que le chantier de la base navale de Bordeaux bat son plein et a grandement besoin de main d'œuvre, le régime de Vichy, ne voulant pas exposer les français, livre les républicains espagnols, les “indésirables”,  aux forces allemandes. Les conditions de travail sont insoutenables. Menacés par des attaques, des bombardements et des tirs ennemis, ils travaillaient 11h par jour sous la stricte surveillance des SS, avant d'être renvoyés dans la caserne Niels, aujourd’hui dénommée l’espace Darwin, sur la rive droite de Bordeaux, où ils étaient détenus. Les ouvriers coulaient du béton 24h sur 24. Durant ce chantier, plus de 70 républicains espagnols sont décédés, certains auraient été emmurés dans l’édifice. <br><br>


En hommage aux hommes disparus durant cette construction, un Mémorial a été inauguré devant la base le 14 avril 2012. En 2015, Manuel Valls a reconnu coupable la France d’avoir déporté les Espagnols dans des camps de concentration en 1939. Avant ça, la Retirada n’avait jamais été assumée par la France. Mais la prise de parole du premier ministre n’avait été publiée dans aucune autre presse. 
D’autre part, à l'intérieur des bassins de lumières, dans l’exposition permanente, seule une demi phrase est adressée aux républicains espagnols. Est-ce donc suffisant ? Ces hommes, suite à la défaite d’une guerre, se retrouvent dans un pays inconnu, enfermés dans des camps, puis, aux profit des français, livrés aux forces ennemies et ensuite contraints aux travaux forcés. Certains se sont par la suite liés à la Résistance, et se sont battus aux mêmes titres que des milliers de français contre le régime nazis. Malgré ça, l’histoire de ces oubliés aux destins tragiques reste enfouie dans l’ombre. 
A ce jour, les descendants de ces exilés espagnols cherchent encore à retracer l’histoire de leurs ancêtres républicains dont l’ombre plane encore sur certains aspects. <br><br>


            </p>
            <h2>
            Conclusion
            </h2>
            <p>
            Dans une Histoire où la France est loin d’être immaculée, les Bassins de Lumières semblent représenter la rédemption de ce lieu. Recouvrir ses murs, teinté du destin tragique de ces hommes, par une multitude de couleurs et de formes. Loin de là l’intention de dévaloriser ce travail faramineux qui à permis à cet endroit de devenir une toile géante, émerveillant les publics de tout âge. La structure des Bassins de Lumières a offert de nouvelles possibilités en termes d’interprétation des œuvres classiques. Mais les peintures se reflétant dans l’eau ne suffisent pas à faire oublier le sang qui y a coulé, ces gens qui y ont laissé la vie, et dont la destinée est encore trop peu représentée. Faudra - t -il attendre longtemps pour que la France assume l’entièreté de son passé, ou les actes resteront enfouis dans les mémoires de certains ?
        </p>

                Il fait partie intégrante de chaque ville et pourtant il reste malgré tout illégal. Cet art génère cependant une grande fascination chez de nombreux artistes et n’est pas prêt de s’éteindre ! Découvrez ce qu’il en est à Bordeaux et ses artistes phare tels que Alber ou encore Michaël Husser, un street artiste venu habiller les murs de la belle endormie.
            </p>
            <p style="font-weight: bold;">
                Êtes-vous prêts à découvrir cette face cachée de Bordeaux ?
            </p>
            <h2>
                Reprenons les bases
            </h2>
            <p>
                Le street art ( art urbain en français ), définit toutes les fresques et les peintures que l’on aperçoit aux quatre coins des murs de nos villes. Ils sont généralement réalisés à partir de peintures aérosol. Mais il regroupe différentes autres techniques comme la mosaïque, le pochoir ou encore les stickers.
L’histoire de cette pratique commence à New York dans les années 40. Où un ouvrier dans une usine de peinture aérosol s’est amusé à signer sur un mur de cette même usine : “Kilroy was here”. Vous connaissiez sûrement cette phrase sans connaître son origine ! Aujourd’hui, on la retrouve dans tout le globe, on peut même l’apercevoir dans le jeu vidéo Life is strange. Par ailleurs, en France nous verrons le street art se répandre qu’à partir des années 70. Malgré l’engouement que suscite cet art, le street art demeure illégal, il est passible d’amende allant de 3 750 € jusqu’à 30 000 €, mais aussi de travaux généraux et même d’emprisonnement. De quoi passer l’envie de s’essayer au street art sans autorisation. Mais ce n’est pas ce qui effraie nos street artist, pour le plus grand bien de nos pupilles !
            </p>
            <h2>
                Alber, l’artiste au milles visages :
            </h2>
            <p>
                Tous ces interdits n’ont pas empêché Alber, un artiste graffeur bordelais de commencer les graffiti à l’âge de 15 ans seulement. Qui depuis 2010 remplies les bâtiments bordelais de ces gigantesques œuvres remplies de couleurs. Si vous êtes un habitué de Bordeaux, ses œuvres ne vous ont sûrement pas échappé. Alber oriente son travail autour du visage, parmi toutes ces faces la seule que l'on ne voit jamais, c’est la sienne. Il préfère donner plus de visibilité à ses visages peints, composés de formes géométriques aux couleurs vives. Toujours impressionnantes, avec leurs regards aussi perçants et émotifs les uns que les autres, leurs formes tout en courbes et en douceur, tantôt des femmes tantôt des hommes mais toujours aux aplats de couleurs bariolés et excentriques. Son art, comparé à d'autres artistes n’a rien de politique, son seul but, colorer les murs ternes de Bordeaux ! Ses visages se retrouvent un peu partout dans Bordeaux et même en intérieur, il n’est donc pas difficile de croiser leur regard. Mais si vous n’avez jamais eu la chance de rencontrer l’une de ces fresques ou si vous voulez en découvrir d’avantage, voici plusieurs spots où vous pouvez les trouver et les admirer : Du quai Darwin aux Bassins à flots mais aussi sur les devantures de commerces aux quartiers Saint-Pierre et Saint-Michel. Amateur d’art ou simplement promeneur curieux, levez les yeux en espérant croiser la route d’une de ces œuvres éphémères, et oui, les non adeptes de street art n’ont aucuns remords à effacer ces œuvres qualifié de vandalisme !

            </p>
            <p>
                Malgré la loi, le street art est une pratique artistique à part entière, il y a autant de styles que d’artistes. Et à Bordeaux, cet art ne manque pas d’adeptes, tel que Michaël Husser alias Mika dont les œuvres surréalistes se retrouvent en devantures de magasin dans le centre de la ville, mais aussi dans les coins plus reculés et tranquilles. Laissons de côté les couleurs vives des visages d’Alberts pour nous intéresser aux personnages attendrissants de Mika. Chacunes de ces œuvres semblent véhiculer une émotion différente, un enfants aux masque de chat et au cheveux fleurit ou bien des femmes masquées ornés de feuilles de vignes, laissez vous emporter dans le monde unique de cet artiste. Son talent n’a d'ailleurs pas laissé les entreprises indifférentes puisqu'il a, à plusieurs reprises, travaillé pour Decathlon dans le design de skateboard, de snowboard ou encore de doudoune. Mais également pour Citroën lui a fait confiance pour graffer la voiture “DS3 Cabrio” à l’occasion d’un shooting photo. A Bordeaux, c’est à la Chartreuse Bel Sito que vous trouverez le plus grand condensé de ses œuvres. De très larges fresques surréalistes remplies d'émotions. Ses œuvres poétiques s’étendent jusqu’au chantier désaffecté qui se trouve à une centaine de mètres.


            </p>
            <p>
                Tous ces bâtiments qui ne servent plus et que tout le monde oublie sont un véritable trésor d’expression artistique. Cet art reste malgré tout illégal et présente un fort risque de pénalisation. Il est parfois utilisé pour véhiculer certains messages de propagandes ou de propos très déplacés, mais souvenez-vous qu’ils n’ont rien en commun avec ces grandes œuvres qui redonnent des couleurs là où il n’y en a plus et ravive l’âme déchue de ces lieux pourtant extraordinaires. N’attendez plus, partez à la rencontre des visages colorés d’Albert ou bien des personnages poétiques de Mika, peut être tomberez vous nez à nez avec une oeuvres nouveau née.
            </p>
